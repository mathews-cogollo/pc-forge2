function crearContenidoHTML() {
  var container = document.querySelector('.planos-container');
  var planosHTML = '';

  // Recorrer cada plano
  datosplano.planos.forEach(function(plano) {
      var html = '<div class="plano">';
      html += '<h2>' + plano.nombrea + '</h2>';
      html += '<p>Fecha: ' + plano.fecha + '</p>';
      html += '<p>Usuario ID: ' + plano.usuario_id_usuario + '</p>';

      // Recorrer los componentes
      html += '<div class="componentes-flotantes">';
      html += '<ul>';
      plano.componentes.forEach(function(componente) {
          html += '<li>';
          html += '<strong>' + componente.nombre_componente + '</strong>';
          html += 'Marca: ' + componente.marca + '<br>';
          html += 'Capacidad: ' + componente.capacidad + '<br>';
          html += 'Precio: $' + componente.precio.toFixed(2) + '<br>';
          html += 'Público Objetivo: ' + componente.publico_objetivo + '<br>';
          html += '</li>';
      });
      html += '</ul>';
      html += '</div>'; // Cierre de .componentes-flotantes
      html += '</div>'; // Cierre de .plano

      planosHTML += html;
  });

  container.innerHTML = planosHTML;
}

// Llamar a la función para crear el contenido HTML
crearContenidoHTML();

